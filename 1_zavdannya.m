% Крок 1: Підготовка даних
X = [-0.3 -2.0 -2.0 -0.1; 
      1.5  1.0 -2.0 -1.0]; % Вхідні вектори (2x4)
T = [0 1 1 0];            % Цільові класи (1x4)

% Крок 2: Нормування даних до інтервалу [0,1]
X_max = max(abs(X), [], 2); % Максимальні значення для кожної ознаки
X_normalized = X ./ X_max;  % Нормування

% Крок 3: Створення персептрона
net = perceptron;
net.layers{1}.transferFcn = 'hardlim';         % Функція активації
net.inputWeights{1,1}.learnFcn = 'learnpn';    % Навчальне правило
net.trainParam.epochs = 1000;                  % Максимальна кількість епох

% Крок 4: Навчання мережі
net = train(net, X_normalized, T);

% Крок 5: Перевірка результатів
Y = sim(net, X_normalized); % Вихід мережі
disp('Результати класифікації:');
disp(Y);

disp('Цільові класи:');
disp(T);
